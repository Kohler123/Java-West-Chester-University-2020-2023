public class Broker {

    private String name;

    public Broke(String name) {

        this.name;
    }

    public void buy(String symbol, int quantity) {
        System.out.println(name + " bought " + quantity + " shares of " + symbol);
    }

    public void sell(String symbol, int quantity) {
        System.out.println(name + " sold " + quantity + " shares of " + symbol);
    }

}